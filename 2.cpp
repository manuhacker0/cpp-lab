//C++ program to illustrate data types
#include <iostream>
using namespace std;
int main()
{
    int a = 5;
    float b = 5.22;
    char c = 'd';
    double d;
    long int e;
    bool f = true;
    cout << "integer data type a= " << a << endl;
    cout << "float data type a= " << b << endl;
    cout << "char data type a= " << c << endl;
    cout << "double data type a= " << d << endl;
    cout << "long data type a= " << e << endl;
    cout << "bool data type a= " << f << endl;
    return 0;
}